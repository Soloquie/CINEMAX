package com.uniquindio.CINEMAX.Persistencia.Entity;
/* Enumeración que representa los posibles estados de un carrito de compras en el sistema CINEMAX.
 * Los estados incluyen ACTIVO (carrito en uso), CHECKOUT (carrito en proceso de compra) y ABANDONADO (carrito dejado sin finalizar).
  */
public enum EstadoCarrito {
    ACTIVO, CHECKOUT, ABANDONADO
}