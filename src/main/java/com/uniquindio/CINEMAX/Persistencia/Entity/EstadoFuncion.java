package com.uniquindio.CINEMAX.Persistencia.Entity;
/* Enumeración que representa los posibles estados de una función en el sistema CINEMAX.
 * Los estados incluyen PROGRAMADA (función programada para una fecha y hora), CANCELADA (función cancelada)
 *  y FINALIZADA (función que ya ha ocurrido).
  */
public enum EstadoFuncion {
    PROGRAMADA, CANCELADA, FINALIZADA
}